// Get current language (sync - usa cache)
function getCurrentLanguage() {
    return _currentLanguage;
}

// Set language (async - salva no storage)
function setLanguage(lang) {
    _currentLanguage = lang;
    chrome.storage.local.set({ 'vectorizer-language': lang });
}

// Get translation
function t(key) {
    const lang = getCurrentLanguage();
    const locale = LOCALES[lang] || LOCALES.en;
    return locale.strings[key] || LOCALES.en.strings[key] || key;
}

// Get all available languages
function getAvailableLanguages() {
    return Object.entries(LOCALES).map(([code, locale]) => ({
        code,
        name: locale.name,
        flag: locale.flag
    }));
}

